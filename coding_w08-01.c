#include <stdio.h>

int main(){
    int i = 2;      

while (i <= 10){
    printf("%d ",i);
    i += 2;
}
printf("\nEnd of loop");
    return 0;
}


/*กำหนดค่า i=2 ตรวจสอบเงื่อนไข i <= 10 
รับค่า i มาแล้ว อยู่ในเงื่อนไขก็จะเพิ่มค่า i+=2 ค่าจะเป็น 4
วนลูปเหมือนเดิม จะเป็นแสดงลำดับเลขคู่ จนค่า i=10 
เงื่อนไข i <= 10  รับค่า i มาแล้วอยู่ในเงื่อนไขก็จะมาเจอค่า i+=2 
ค่าจะเป็น 12 จะออกจากลูปแล้วแสดง End of loop */